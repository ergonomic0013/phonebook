<?php

namespace AppBundle\Entity;

/**
 * Session_msg
 */
class Session_msg
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $sessionMsg;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set sessionMsg
     *
     * @param string $sessionMsg
     *
     * @return Session_msg
     */
    public function setSessionMsg($sessionMsg)
    {
        $this->sessionMsg = $sessionMsg;

        return $this;
    }

    /**
     * Get sessionMsg
     *
     * @return string
     */
    public function getSessionMsg()
    {
        return $this->sessionMsg;
    }
}
